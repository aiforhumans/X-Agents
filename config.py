def validate_lm_studio_connection() -> bool:
    """
    ğŸ” Validate if LM Studio is accessible
    
    ğŸ“‹ Returns:
        âœ… True if connection is successful, False otherwise
    """
    try:
        import requests
        config = get_lm_studio_config()
        
        # â”Œâ”€ Test Connection â”€â”
        response = requests.get(
            f"{config['openai_api_base']}/models",
            timeout=5
        )
        return response.status_code == 200
        
    except Exception:
        return False

def get_available_models() -> list:
    """
    ğŸ“‹ Get list of available models from LM Studio
    
    ğŸ“‹ Returns:
        ğŸ“Š List of model identifiers
    """
    try:
        import requests
        config = get_lm_studio_config()
        
        # â”Œâ”€ Fetch Models â”€â”
        response = requests.get(
            f"{config['openai_api_base']}/models",
            timeout=5
        )
        
        if response.status_code == 200:
            data = response.json()
            return [model["id"] for model in data.get("data", [])]
        else:
            return []
            
    except Exception:
        return []

